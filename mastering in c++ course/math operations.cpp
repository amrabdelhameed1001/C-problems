#include<iostream>
using namespace std;

int main() {
	int a;
	cout << "Enter first number: ";
	cin >> a;

	int b;
	cout << "Enter seconed number: ";
	cin >> b;

	cout << a << " + " << b << " = " << a + b << endl; 
	cout << a << " - " << b << " = " << a - b << endl;
	cout << a << " / " << b << " = " << a / b << endl;
	cout << a << " * " << b << " = " << a * b << endl;

	return 0;

}
